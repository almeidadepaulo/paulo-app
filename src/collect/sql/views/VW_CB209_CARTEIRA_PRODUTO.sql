-- Apaga view
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VW_CB209_CARTEIRA_PRODUTO]'))
DROP VIEW [dbo].VW_CB209_CARTEIRA_PRODUTO
GO

-- Tela Carteira - Carteira por Banco
-- Cria View
CREATE VIEW [dbo].VW_CB209_CARTEIRA_PRODUTO
WITH ENCRYPTION
AS
 SELECT 
	CB209_NR_OPERADOR,
	CB209_NR_CEDENTE,
	CB209_CD_COMPSC,	
	CB209_CD_CART,
	CB209_NR_PRODUT,
	CB209_DT_MOVTO ,
	SUM(CB209_VL_VALOR)		CB209_VL_VALOR ,
	SUM(CB209_QT_CONTRA)	CB209_QT_CONTRA,
	SUM(CB209_QT_TOTAL)		CB209_QT_TOTAL,
	-- BANCO
	CB250_NM_BANCO,
	-- PRODUTO
	CB255_DS_PROD,
	-- CARTEIRA
	CB256_DS_CART
	

 FROM CB209 AS CB209

LEFT OUTER JOIN CB250 AS CB250  -- BANCO
ON     CB250_NR_OPERADOR = CB209_NR_OPERADOR
   AND CB250_NR_CEDENTE  = CB209_NR_CEDENTE
   AND CB250_CD_COMPSC	 = CB209_CD_COMPSC

 LEFT OUTER JOIN CB255 AS CB255  -- PRODUTO
ON     CB255_NR_OPERADOR = CB209_NR_OPERADOR
   AND CB255_NR_CEDENTE  = CB209_NR_CEDENTE
   AND CB255_CD_PROD	 = CB209_NR_PRODUT

  LEFT OUTER JOIN CB256 AS CB256  -- CARTEIRA
ON     CB256_NR_OPERADOR = CB209_NR_OPERADOR
   AND CB256_NR_CEDENTE  = CB209_NR_CEDENTE
   AND CB256_CD_CART     = CB209_CD_CART
 
 WHERE  CB209_ID_TIPOAC = 10 /* SALDO ATUAL */

 GROUP BY 
	CB209_NR_OPERADOR  ,
	CB209_NR_CEDENTE,
	CB209_DT_MOVTO ,
	CB209_CD_COMPSC,
	CB209_CD_CART,
	CB209_NR_PRODUT,
	CB250_NM_BANCO ,
	CB255_DS_PROD  ,
	CB209_CD_CART  ,
	CB256_DS_CART

GO

