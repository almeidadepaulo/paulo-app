-- Apaga view
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VW_CB209_ENTRADA]'))
DROP VIEW [dbo].[VW_CB209_ENTRADA]
GO

-- Tela Carteira - Entrada por Banco
-- Cria View
CREATE VIEW [dbo].[VW_CB209_ENTRADA]
WITH ENCRYPTION
AS
 SELECT 
	CB209_NR_OPERADOR,
	CB209_NR_CEDENTE,
	CB209_DT_MOVTO ,
	SUM(CB209_VL_VALOR)		CB209_VL_VALOR ,
	SUM(CB209_QT_CONTRA)	CB209_QT_CONTRA,
	SUM(CB209_QT_TOTAL)		CB209_QT_TOTAL,
	CB209_CD_COMPSC,

	-- BANCO
	CB250_CD_COMPSC,
	CB250_NM_BANCO

 FROM CB209 AS CB209

LEFT OUTER JOIN CB250 AS CB250  -- BANCO
ON     CB250_NR_OPERADOR = CB209_NR_OPERADOR
   AND CB250_NR_CEDENTE  = CB209_NR_CEDENTE
   AND CB250_CD_COMPSC	 = CB209_CD_COMPSC 

 WHERE  CB209_ID_TIPOAC  = 2 /* ENTRADAS */

 GROUP BY 
  CB209_DT_MOVTO,
  CB209_NR_OPERADOR,
  CB209_NR_CEDENTE,
  CB209_CD_COMPSC,
  CB250_CD_COMPSC,
  CB250_NM_BANCO 
	
GO

