-- Apaga view
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VW_CB066]'))
DROP VIEW [dbo].[VW_CB066]
GO

-- Cria View
CREATE VIEW [dbo].[VW_CB066]
WITH ENCRYPTION
AS
  SELECT
    CB066.CB066_NR_INST,
  	CB066.CB066_CD_POSTAG,
	CB066.CB066_CD_PUBLIC,
  	CB066.CB066_CD_FORMAT,
  	CB066.CB066_CD_OPESIS,
  	CB066.CB066_DT_INCSIS,
  	CB066.CB066_DT_ATUSIS,
  
    CB050.CB050_NR_INST,
  	CB050.CB050_NM_INST,
  	CB050.CB050_NM_INSTR,
  	CB050.CB050_CD_OPESIS,
  	CB050.CB050_DT_INCSIS,
  	CB050.CB050_DT_ATUSIS,
    Cast(CB050.CB050_NR_INST as varchar) +' - '+ CB050.CB050_NM_INST AS CB050_NR_NM_INST,
    
    CB060.CB060_DS_PUBLIC,
    
    CB061.CB061_DS_FORMAT,
    
    CB065.CB065_DS_POSTAG
    
  FROM CB066 AS CB066
  
  LEFT OUTER JOIN CB060 AS CB060
  ON CB066.CB066_CD_PUBLIC = CB060.CB060_CD_PUBLIC
  AND CB066.CB066_NR_INST = CB060.CB060_NR_INST
  
  LEFT OUTER JOIN CB065 AS CB065
  ON CB066.CB066_CD_POSTAG = CB065.CB065_CD_POSTAG
  AND CB066.CB066_NR_INST = CB065.CB065_NR_INST
  
  LEFT OUTER JOIN CB061 AS CB061
  ON CB066.CB066_CD_FORMAT = CB061.CB061_CD_FORMAT
  AND CB066.CB066_NR_INST = CB061.CB061_NR_INST
  
  LEFT OUTER JOIN CB050 AS CB050
  ON CB066.CB066_NR_INST = CB050.CB050_NR_INST -- Teste
  
GO