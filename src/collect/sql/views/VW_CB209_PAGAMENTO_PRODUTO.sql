-- Apaga view
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VW_CB209_PAGAMENTO_PRODUTO]'))
DROP VIEW [dbo].[VW_CB209_PAGAMENTO_PRODUTO]
GO

-- Entrada por Produto
-- Cria View
CREATE VIEW [dbo].[VW_CB209_PAGAMENTO_PRODUTO]
WITH ENCRYPTION
AS
 SELECT 
	CB209_NR_OPERADOR,
	CB209_NR_CEDENTE,
	CB209_DT_MOVTO ,
	SUM(CB209_VL_VALOR)		CB209_VL_VALOR ,
	SUM(CB209_QT_TOTAL)		CB209_QT_TOTAL,
	SUM(CB209_QT_CONTRA)	CB209_QT_CONTRA,
	SUM(CB209_VL_ACRE)		CB209_VL_ACRE,
	(SUM(CB209_VL_DESC) + SUM(CB209_VL_ABATI)) CB209_VL_DESC,
	(SUM(CB209_VL_VALOR) + SUM(CB209_VL_ACRE) - (SUM(CB209_VL_DESC) + SUM(CB209_VL_ABATI))) CB209_VL_TOTAL,
	CB209_CD_COMPSC,
	CB209_ID_TIPOAC,
	-- BANCO
	CB250_CD_COMPSC,
	CB250_NM_BANCO,

	-- CARTEIRA
	CB256_CD_CART,
	CB256_DS_CART,

	-- PRODUTO
	CB255_CD_PROD,
	CB255_DS_PROD



 FROM CB209 AS CB209

LEFT OUTER JOIN CB250 AS CB250  -- BANCO
ON     CB250_NR_OPERADOR = CB209_NR_OPERADOR
   AND CB250_NR_CEDENTE  = CB209_NR_CEDENTE
   AND CB250_CD_COMPSC	 = CB209_CD_COMPSC

  LEFT OUTER JOIN CB256 AS CB256  -- CARTEIRA
ON     CB256_NR_OPERADOR = CB209_NR_OPERADOR
   AND CB256_NR_CEDENTE  = CB209_NR_CEDENTE
   AND CB256_CD_CART     = CB209_CD_CART

 LEFT OUTER JOIN CB255 AS CB255  -- PRODUTO
 ON    CB255_NR_OPERADOR = CB209_NR_OPERADOR
   AND CB255_NR_CEDENTE  = CB209_NR_CEDENTE
   AND CB255_CD_PROD	 = CB209_NR_PRODUT

 GROUP BY 
  CB209_DT_MOVTO,
  CB209_NR_OPERADOR,
  CB209_NR_CEDENTE,
  CB209_CD_COMPSC,
  CB209_ID_TIPOAC,
  CB250_CD_COMPSC,
  CB250_NM_BANCO,
  CB256_CD_CART,
  CB256_DS_CART,
  CB255_CD_PROD,
  CB255_DS_PROD

GO

