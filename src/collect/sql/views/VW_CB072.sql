CREATE VIEW [dbo].[VW_CB072]
WITH ENCRYPTION
AS
  SELECT
    CB072.CB072_NR_INST,
  	CB072.CB072_CD_REGIAO,
	CB072.CB072_NR_CEPINI,
	CB072.CB072_NR_CEPFIM,
  	CB072.CB072_CD_OPESIS,
  	CB072.CB072_DT_INCSIS,
  	CB072.CB072_DT_ATUSIS,
  
    CB050.CB050_NR_INST,
  	CB050.CB050_NM_INST,
  	CB050.CB050_NM_INSTR,
  	CB050.CB050_CD_OPESIS,
  	CB050.CB050_DT_INCSIS,
  	CB050.CB050_DT_ATUSIS,
    Cast(CB050.CB050_NR_INST as varchar) +' - '+ CB050.CB050_NM_INST AS CB050_NR_NM_INST,
    
    CB070.CB070_DS_REGIAO
    
  FROM CB072 AS CB072
  
