  ON CB072.CB072_CD_REGIAO = CB070.CB070_CD_REGIAO
  AND CB072.CB072_NR_INST = CB070.CB070_NR_INST
  
  LEFT OUTER JOIN CB050 AS CB050
  ON CB072.CB072_NR_INST = CB050.CB050_NR_INST -- Teste

