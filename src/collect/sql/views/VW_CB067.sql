-- Cria View
CREATE VIEW [dbo].[VW_CB067]
WITH ENCRYPTION
AS
  SELECT
    CB067.CB067_NR_INST,
  	CB067.CB067_CD_POSTAG,
  	CB067.CB067_CD_REGIAO,
  	CB067.CB067_QT_PRAZO,
  	CB067.CB067_CD_OPESIS,
  	CB067.CB067_DT_INCSIS,
  	CB067.CB067_DT_ATUSIS,
  
    CB050.CB050_NR_INST,
  	CB050.CB050_NM_INST,
  	CB050.CB050_NM_INSTR,
  	CB050.CB050_CD_OPESIS,
  	CB050.CB050_DT_INCSIS,
  	CB050.CB050_DT_ATUSIS,
    Cast(CB050.CB050_NR_INST as varchar) +' - '+ CB050.CB050_NM_INST AS CB050_NR_NM_INST,
    
    CB070.CB070_DS_REGIAO,
    
    CB065.CB065_DS_POSTAG
    
  FROM CB067 AS CB067
  
  LEFT OUTER JOIN CB065 AS CB065
  ON CB067.CB067_CD_POSTAG = CB065.CB065_CD_POSTAG
  AND CB067.CB067_NR_INST = CB065.CB065_NR_INST
  
  LEFT OUTER JOIN CB070 AS CB070
  ON CB067.CB067_CD_REGIAO = CB070.CB070_CD_REGIAO
  AND CB067.CB067_NR_INST = CB070.CB070_NR_INST
  
  LEFT OUTER JOIN CB050 AS CB050
  ON CB067.CB067_NR_INST = CB050.CB050_NR_INST -- Teste
  
GO