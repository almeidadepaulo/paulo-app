-- Cria View
CREATE VIEW [dbo].[VW_CB085]
WITH ENCRYPTION
AS
  SELECT
    CB085.CB085_NR_INST,
  	CB085.CB085_CD_EMIEMP,
  	CB085.CB085_CD_TARIFA,
  	CB085.CB085_DS_TARIFA,
  	CB085.CB085_DS_TARIFR,
  	CB085.CB085_VL_PADRAO,
  	CB085.CB085_CD_OPESIS,
  	CB085.CB085_DT_INCSIS,
  	CB085.CB085_DT_ATUSIS,
  
    CB050.CB050_NR_INST,
  	CB050.CB050_NM_INST,
  	CB050.CB050_NM_INSTR,
  	CB050.CB050_CD_OPESIS,
  	CB050.CB050_DT_INCSIS,
  	CB050.CB050_DT_ATUSIS,
    Cast(CB050.CB050_NR_INST as varchar) +' - '+ CB050.CB050_NM_INST AS CB050_NR_NM_INST,
    
    CB053.CB053_DS_EMIEMP
    
  FROM CB085 AS CB085
  LEFT OUTER JOIN CB050 AS CB050
  ON CB085.CB085_NR_INST = CB050.CB050_NR_INST -- Teste
  
  LEFT OUTER JOIN CB053 AS CB053
  ON CB085.CB085_CD_EMIEMP = CB053.CB053_CD_EMIEMP
  AND CB085.CB085_NR_INST = CB053.CB053_NR_INST

GO