-- Cria View
CREATE VIEW [dbo].[VW_CB075]
WITH ENCRYPTION
AS
  SELECT
    CB075.CB075_NR_INST,
  	CB075.CB075_NR_CHAVE,
  	CB075.CB075_NR_REGION,
  	CB075.CB075_NR_CONTRA,
  	CB075.CB075_NR_ULTLOT,
  	CB075.CB075_NR_OBJPOS,
  	CB075.CB075_CD_OPESIS,
  	CB075.CB075_DT_INCSIS,
  	CB075.CB075_DT_ATUSIS,
  
    CB050.CB050_NR_INST,
  	CB050.CB050_NM_INST,
  	CB050.CB050_NM_INSTR,
  	CB050.CB050_CD_OPESIS,
  	CB050.CB050_DT_INCSIS,
  	CB050.CB050_DT_ATUSIS,
    Cast(CB050.CB050_NR_INST as varchar) +' - '+ CB050.CB050_NM_INST AS CB050_NR_NM_INST
    
  FROM CB075 AS CB075
  
  LEFT OUTER JOIN CB050 AS CB050
  ON CB075.CB075_NR_INST = CB050.CB050_NR_INST -- Teste

GO