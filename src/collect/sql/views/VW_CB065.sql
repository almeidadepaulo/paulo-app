-- Apaga view
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VW_CB065]'))
DROP VIEW [dbo].[VW_CB065]
GO

-- Cria View
CREATE VIEW [dbo].[VW_CB065]
WITH ENCRYPTION
AS
  SELECT
    CB065.CB065_NR_INST,
  	CB065.CB065_CD_POSTAG,
  	CB065.CB065_DS_POSTAG,
  	CB065.CB065_NR_FAC,
  	CB065.CB065_ID_FAC,
	CASE 
	WHEN CB065.CB065_ID_FAC = 'S' THEN 'Simples'
	WHEN CB065.CB065_ID_FAC = 'R' THEN 'Registrado'
	ELSE CB065.CB065_ID_FAC
	END CB065_ID_FAC_L,
  	CB065.CB065_CD_OPESIS,
  	CB065.CB065_DT_INCSIS,
  	CB065.CB065_DT_ATUSIS,
  
    CB050.CB050_NR_INST,
  	CB050.CB050_NM_INST,
  	CB050.CB050_NM_INSTR,
  	CB050.CB050_CD_OPESIS,
  	CB050.CB050_DT_INCSIS,
  	CB050.CB050_DT_ATUSIS,
    Cast(CB050.CB050_NR_INST as varchar) +' - '+ CB050.CB050_NM_INST AS CB050_NR_NM_INST
    
  FROM CB065 AS CB065
  
  LEFT OUTER JOIN CB050 AS CB050
  ON CB065.CB065_NR_INST = CB050.CB050_NR_INST -- Teste

GO