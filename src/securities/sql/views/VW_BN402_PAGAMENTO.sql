-- Tela Carteira - Pagemento
-- Cria View
CREATE VIEW [dbo].[VW_BN402_PAGAMENTO]
WITH ENCRYPTION
AS
 SELECT 
	BN402_NR_GRUPO,
	BN402_NR_INST,
	BN402_DT_MOVTO ,
	SUM(BN402_VL_VALOR)		BN402_VL_VALOR ,
	--SUM(CB209_QT_CONTRA)	CB209_QT_CONTRA,
	SUM(BN402_QT_TOTAL)		BN402_QT_TOTAL,
	BN402_NR_BANCO,

	-- BANCO
	BN007_NR_BANCO,
	BN007_NM_BANCO,

	-- CESSIONÁRIO
	BN001_NM_GRUPO

 FROM BN402 AS BN402

 LEFT OUTER JOIN BN001
 ON BN001_NR_GRUPO = BN402_NR_GRUPO -- CESSIONÁRIO

 LEFT OUTER JOIN BN007 AS BN007  -- BANCO
 ON  BN007_NR_BANCO	=	BN402_NR_BANCO 

 WHERE BN402_TP_ACUM  = 3 /* LIQUIDADOS */

 GROUP BY 
  BN402_DT_MOVTO,
  BN402_NR_GRUPO,
  BN402_NR_INST,
  BN402_NR_BANCO,
  BN007_NR_BANCO,
  BN007_NM_BANCO,

  -- CESSIONÁRIO
  BN001_NM_GRUPO 
	
GO

