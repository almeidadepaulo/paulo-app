-- Apaga view
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VW_BN017_SUBCLASSE]'))
DROP VIEW [dbo].[VW_BN017_SUBCLASSE]
GO

-- Cria View
CREATE VIEW [dbo].[VW_BN017_SUBCLASSE]
WITH ENCRYPTION
AS
  SELECT
  
  BN017_NR_SECAO,
  BN017_NR_CLASSE,
  BN017_NR_SUBCLA

FROM 	BN017 

GROUP BY
BN017_NR_SECAO,
BN017_NR_CLASSE,
BN017_NR_SUBCLA
