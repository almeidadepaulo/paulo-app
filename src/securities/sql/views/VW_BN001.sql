-- Apaga view
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VW_BN001]'))
DROP VIEW [dbo].[VW_BN001]
GO

-- Cria View
CREATE VIEW [dbo].[VW_BN001]
WITH ENCRYPTION
AS
  SELECT -- 
  BN001.BN001_NR_GRUPO,
  BN001.BN001_NR_CNPJ,
  BN001.BN001_NM_GRUPO,
  BN001.BN001_NR_OPESIS,
  BN001.BN001_DT_INCSIS,
  BN001.BN001_DT_ATUSIS,    
  Cast(BN001.BN001_NR_GRUPO as varchar)+' - '+BN001.BN001_NM_GRUPO AS BN001_NR_NM_GRUPO
  
  FROM BN001 AS BN001

GO


