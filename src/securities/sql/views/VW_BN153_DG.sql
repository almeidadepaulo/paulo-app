-- Relatório Diário de Garantias

-- Cria View
CREATE VIEW [dbo].[VW_BN153_DG]
WITH ENCRYPTION
AS
SELECT
  BN153.BN153_NR_GRUPO,
  BN153.BN153_NR_INST,
  BN153.BN153_DT_BASE,
  BN153.BN153_NR_PROD,
  BN153.BN153_NR_LOTE,
  BN153.BN153_NR_CONTRA,
  BN153.BN153_DT_PVCTO,
  BN153.BN153_DT_UVCTO,
  BN153.BN153_QT_PAREX,
  BN153.BN153_VL_PARC,
  BN153.BN153_TT_NOM,
  BN153.BN153_TT_PRES,
  BN153.BN153_TT_EXIG,
  BN153.BN153_DT_INCSIS,
  BN150.BN150_NR_CONTA,
  BN150.BN150_NM_CLIENT,
--  BN150.BN403_ST_PAGTO,

-- CONTRATO
 BN150.BN150_TP_CPFCNPJ,
  ISNULL(BN150.BN150_NR_CPFCNPJ,'0') AS BN150_NR_CPFCNPJ,
 

-- CONTA/ENTIDADE
  BN009.BN009_NR_BANCO,
  BN009.BN009_NR_AGENC,
  BN009.BN009_NR_CONTA,
  BN009.BN009_NM_CONTA,
  
  BN002.BN002_NM_INST

  ,VW_BN201.BN200_NM_EEMCCB
  ,VW_BN201.BN201_NR_CNPJ
  ,VW_BN201.BN201_ST_CONTRATO

  ,(SELECT
			COUNT(*)
		 FROM BN201 AS BN201
		 
		 INNER JOIN BN004 AS BN004
		 ON BN004.BN004_NR_PROD = BN201.BN201_NR_PROD
		 
		 WHERE 1=1
		 AND BN201_NR_LOTE		= BN153.BN153_NR_LOTE		 		
		 --AND BN201_ST_CONTRATO  IN ('L','C','N')
		 
		 AND BN004_TP_PROD = 'B'  
	) AS CCB_COUNT
 
FROM BN153 AS BN153


LEFT OUTER JOIN BN002 AS BN002 -- INSTITUICAO
ON  BN002.BN002_NR_GRUPO   = BN153.BN153_NR_GRUPO
AND BN002.BN002_NR_INST    = BN153.BN153_NR_INST

 
LEFT OUTER JOIN BN150 AS BN150 -- CONTRATO
ON  BN150.BN150_NR_CNPJ     = BN002.BN002_NR_CNPJ
AND BN150.BN150_NR_LOTE     = BN153.BN153_NR_LOTE
AND BN150.BN150_NR_CONTRA   = BN153.BN153_NR_CONTRA
AND BN150_TP_MOVTO			= 'D'



LEFT OUTER JOIN BN009 AS BN009  -- CONTA
ON  BN009.BN009_NR_BANCO 	=  BN150.BN150_NR_BANCO
AND BN009.BN009_NR_AGENC 	=  BN150.BN150_NR_AGENC
AND BN009.BN009_NR_CONTA 	=  BN150.BN150_NR_CONTA


-- CCB
LEFT OUTER JOIN VW_BN201 AS VW_BN201
ON	VW_BN201.BN201_NR_GRUPO		= BN153.BN153_NR_GRUPO
AND	VW_BN201.BN201_NR_INST		= BN153.BN153_NR_INST
AND VW_BN201.BN201_NR_LOTE		= BN153.BN153_NR_LOTE
AND VW_BN201.BN201_NR_CONTRAOR  = BN153.BN153_NR_CONTRA


GO
