-- Apagar view
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VW_EM060]'))
DROP VIEW [dbo].[VW_EM060]
GO

CREATE VIEW [dbo].[VW_EM060]
WITH ENCRYPTION
AS

SELECT
  EM060_NR_INST  ,
  CB050_NM_INST  ,
  EM060_CD_EMIEMP,
  CB053_DS_EMIEMP,
  EM060_CD_VAR   ,
  EM060_DS_VAR   ,
  EM060_QT_CARAC ,
  EM060_ID_ATIVO ,
  EM060_ID_ATIVO_label = CASE EM060_ID_ATIVO
    WHEN 1 THEN 'Ativa'      
    ELSE 'Inativa'
  END,  
  EM060_CD_OPESIS,
  EM060_DT_INCSIS,
  EM060_DT_ATUSIS
FROM
  EM060,
  CB050,
  CB053
WHERE
/* INSTITUIÇÃO */
    EM060_NR_INST   = CB050_NR_INST
/* EMISSOR/EMPRESA */
AND EM060_NR_INST   = CB053_NR_INST   
AND EM060_CD_EMIEMP = CB053_CD_EMIEMP


GO