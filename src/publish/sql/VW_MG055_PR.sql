-- Cria View
CREATE VIEW [dbo].[VW_MG055_PR]
WITH ENCRYPTION
AS
  SELECT
    MG055.MG055_NR_INST,
  	MG055.MG055_CD_EMIEMP,
  	MG055.MG055_CD_CODSMS,
  	MG055.MG055_DS_CODSMS,
  	MG055.MG055_ID_ATIVO,
  	MG055.MG055_TP_CATEG,
  	MG055.MG055_CD_OPESIS,
  	MG055.MG055_DT_INCSIS,
  	MG055.MG055_DT_ATUSIS,
  
    MG056.MG056_NR_SEQ
    
  FROM MG055 AS MG055
  LEFT OUTER JOIN MG056 AS MG056
  ON MG055.MG055_CD_CODSMS  = MG056.MG056_CD_CODSMS
  AND MG055.MG055_NR_INST   = MG056.MG056_NR_INST
  AND MG055.MG055_CD_EMIEMP = MG056.MG056_CD_EMIEMP

GO