-- Apagar view
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VW_EM000]'))
DROP VIEW [dbo].VW_EM000
GO

CREATE VIEW [dbo].VW_EM000
WITH ENCRYPTION
AS

SELECT
  EM000_NR_INST   ,
  CB050_NM_INST   ,
  EM000_NM_EMAIL  ,
  EM000_NM_EDMAIL ,
  EM000_NM_SMTP   ,
  EM000_NR_SMTPPO ,
  EM000_ID_SSL    ,
  EM000_NM_USRMAI ,
  EM000_NR_SENMAI ,
  EM000_CD_OPESIS ,
  EM000_DT_INCSIS ,
  EM000_DT_ATUSIS 
FROM
  EM000,
  CB050
WHERE
  EM000_NR_INST = CB050_NR_INST


GO